# Compare:
print('1 == 4', 1 == 4)
print('1 != 4', 1 != 4)
print('1 > 0', 1 > 0)
print('1 <= 0', 1 <= 0)

print("'a' > 'b'", 'a' > 'b')
print("'a' == 'b'", 'a' == 'b')

print('1 is 1', 1 is 1)
print('2 is not 1', 2 is not 1)

# Math:
print('2 ** 4', 2 ** 4)
print('4 % 5', 4 % 5)

# Logical
print('True or False', True or False)
print('False and 10', False and 10)
print('not "st" and (4 or True)', not "st" and (4 or True))
